import { Schema, model } from "mongoose";
import { IUser } from "../shared/interface";

const userSchema = new Schema<IUser>({
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    required: true,
  },
});

export const User = model<IUser>("User", userSchema);
